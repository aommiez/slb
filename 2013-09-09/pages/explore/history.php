    <p style="text-indent:50px;">
        Dating back to the Neolithic civilization situated at the modern-day UNESCO World Heritage Site in Ban Chiang, the history
        of Thailand is long, proud, and fairly well documented.  Over the early centuries of the Common Era, tribes of Mon, Khmer,
        and Tai peoples established realms within the borders of modern Thailand; the Mon speaking Buddhist civilization of
        Dvaravati in the first millennium giving way to the Khmer empire of Angkor by the turn of the second millennium.  
    </p>

    <p style="text-indent:50px;">
    However, the history of Thailand as we know it began when the kingdoms of Lan Na (Chiang Rai/Chiang Mai) and Sukhothai, the
    first truly independent Thai Kingdoms, established highly developed societies in the North and Central regions of Thailand in the
    13th and 14th centuries. The Kingdom of Ayutthaya, which was heavily influenced by the Khmer’s of Angkor, eventually conquered
    neighboring Sukhothai and dominated the region for the next several hundred years of Thai history.  Unfortunately, first Chaing Mai
    and then Ayutthaya were overrun by Burmese invaders, who occupied the Lan Na capital for several centuries and sacked Ayutthaya,
    forcing the central Thai kingdom to relocate farther south, establishing a new capital in Thon Buri near Bangkok.  After the short
    lived Thon Buri Period (1767-1772), the capital was moved across the Chao Phraya River, and the first of the current line of Kings,
    Rama I of the Chakri Dynasty, established the modern capital of Bangkok to commence the Ratanakosin Period of Thai history. 
    </p>
    <p style="text-indent:50px;">
    The adroit diplomatic leadership of Kings Mongkut (Rama IV, 1851-1868) and Chulalongkorn (Rama V, 1868-1910) were responsible
    for maintaining a remarkable 700 year Thai history during which the kingdom was never officially colonized by foreign powers; a
    turbulent 20th century witnessed the transition to a system of constitutional monarchy, currently overseen by Head of State, King
    Bumibol Adulyadej (1946- present), is King Rama IX of the Chakri Dynasty and a tenuous but functional democracy has existed
    under the regency of this much beloved king.
