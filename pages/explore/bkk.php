           <strong> Dress code </strong><br>
            Although Thais are very tolerant especially to foreign tourists,
            it is the best to show respect in dress code when visiting
            temples. This dress code is strictly enforced at the Grand
            Palace. But do not despair, being tourist friendly, the venue
            will provide long loose pants for guys, long wraps and cover
            ups for girls for rent incase you needed. <br>
			<strong>Bangkok Gem Scam </strong><br> 
            As a few others have mentioned, the tourist places are 
			the favorite target for scam. If someone approachs you to buy cheap Gem, be aware that it might be a Scam. <br>
			<strong>Traffic Jams </strong><br>
            Watch out for traffic jams especially in the middle of the city
            (around Siam Kempinski Hotel) and around peak hours (8:00-9:00, 16:00-20:00). 
			You can be jammed up really tight and everything will come down to a crawl if not a complete
            stand still. <br>
			<strong>Moterbike</strong><br>
			Be careful of moterbike while crossing road and on the pavement. <br>
			<strong>Bargaining</strong><br>
			Bargaining is acceptable practice with street vendors.<br>
			<strong>Tipping</strong><br>
			Tipping is not generally expected in Thailand. The exception is loose change for 20-40 Baht from a large bill<br>

