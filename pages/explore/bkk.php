			<strong>Royal Family</strong><br>
            Thai people hold their King and Queen and the Royal Family
            in great reverence, so will not tolerate foreigners showing disrespect to them.

			<strong>Thai Greeting</strong><br>
			 Traditionally, Thais greet each other with a "Wai" by pressing
            the palms together at the chest). If someone Wai you, you
            should Wai back.<br>
			<strong>Thai Smile</strong><br>
		    Thai people smile to express gladness and happiness, to
            thank for small services, to return the Wai of children and
            inferior persons, and even to excuse small inconveniences.<br>
			<strong>Do not!</strong><br>
			- Place your feet on the table while sitting.<br> 
			- Use your foot to point things out or to touch any anyone, this is considered rude. <br>
			- Touch a person's head, nor ruffle his/her hair. The head is
            the noblest part of the body. A sincere apology should be
            offered immediately if you touch someone's head
            unintentionally. <br>
			- Generally Thai women are conservative. So donot touch them
            without their consent.

