            Walking in the city
            At night, it is not a good idea to walk instead of taking a taxi or
            Tuk-Tuk or you will lost your luggage.

            Traffic Jams
            Watch out for traffic jams especially in the middle of the city
            (around Siam Square) and around peak hours (morning and
            evening rush hour traffic). You can be jammed up really tight
            and everything will come down to a crawl if not a complete
            stand still. If you are rushing to the airport around peak hours,
            please give yourself a generous lee way (get to the airport
            early) less you intend to miss your flight and stay another day.

            Heat and humidity
            Be aware of the heat and the humidity in the country before
            you go as it can be extremely hot out there. Do not forget to
            use a lot of sun block to protect your skin and take a shawl to
            cover up when required.
