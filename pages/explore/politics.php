    <p>
        <strong>Government of Thailand</strong>
    </p>

    <div class="section-div">
        <img class="pull-right" src="images/Politics/Image1_209x252.png" style="margin-left: 20px;">
        <p style="text-indent:50px;">Thailand has existed as a modern nation-state since the founding of the Chakri
        Dynasty and the establishment of Bangkok as its capital in 1782.  In 1932, a
        ‘revolution’ absolved the absolute rule of the monarchy and established a
        Constitutional Monarchy, removing the political authority of the crown and
        founding a nascent ‘democracy’.  </p>

        <p style="text-indent:50px;">In 1946, direct elections were finally held in which the people of Thailand voted
        for members of a bicameral legislature (Senate and House of Representatives)
        to be presided over by a Prime Minister representing the executive branch.
        The Judiciary, including a Supreme Court, acts independently of the executive
        office and the legislature, though it was not until the 1996 constitution that more
        effective checks and balances were instituted.</p>
        <div class="clearfix"></div>
    </div>

    <div class="section-div">
        <p><img class="pull-left" style="margin-right: 20px;" src="images/Politics/Image2_205x252.png"></p>
        <p style="text-indent:50px;">From its inception ‘democracy’ in Thailand has been turbulent, with 17 coup
            d’etats passing power back and forth between leaders of the military and an elite
            bureaucracy that borders on plutocracy. The country has also been governed
            under 17 different constitutions; the Kingdom's current constitution the result of
            the most recent coup d’etat, a bloodless overthrow of then Prime Minister
            Thaksin Shinawattra in 2006.  

            </p>
        <p style="text-indent:50px;">Currently, Thailand is embroiled in political wrangling over the implications of that
            coup and the subsequent legislative shake up caused by mass protests both
            against and in favor of the former Prime Minister.  Nonetheless, the Thai people
            are very politically active and value their freedom despite their tenuous democracy. </p>
        <div class="clearfix"></div>
    </div>

    <p>
        <strong>The Monarchy and the King of Thailand</strong>
    </p>

    <div class="section-div">
        <img class="pull-right" src="images/Politics/Image3_192x252.png" style="margin-left: 20px;">
        <p style="text-indent:50px;">Thailand had been ruled outright by kings of various realms since the thirteenth
            century; it was not until 1932 that Thailand became a constitutional monarchy,
            a system not unlike that of the United Kingdom, in which the Thai King serves
            as Head of State and spiritual leader of the country, but wields no outright
            political authority.

            </p>

        <p style="text-indent:50px;">The current King of Thailand, His Majesty King Bhumibol Adulyadej (Rama IX)
            is the ninth Thai king from the House of Chakri, which has ruled Thailand since
            the founding of Bangkok by King Phutthayotfa Chulalok (Rama I) in 1782. H.M.
            King Bhumibol was born in Massachusetts, USA while his father, who did not
            serve as King of Thailand, was attending Harvard University. H.M. Bhumibol
            ascended the throne as King of Thailand in 1946 following the death of his
            brother and has since attained the distinction of being the world’s longest
            reigning monarch and the longest reigning Thai King in Thailand’s history. </p>
        <div class="clearfix"></div>
    </div>

    <div class="section-div">
        <p style="padding-bottom: 20px;" class="pull-left" ><img style="margin-right: 20px;" src="images/Politics/Image379x252.png"></p>
        <p style="text-indent:50px;">While the King of Thailand has little direct power, under the constitution King
            Bhumibol is a symbol of national identity and unity; indeed, the Thai King commands
            enormous popular respect and moral authority, both of which he has leveraged on a
            few rare occasions to resolve political crises that have threatened national stability. 
            In more recent years however, he has maintained a more hands-off approach,
            urging Thais to learn to resolve their differences in an amicable way for the good of
            their country.


        </p>
        <p style="text-indent:50px;">The Thai King and the members of the Royal Family are deeply revered by the Thai
            people for the Royal Family’s passionate commitment to the welfare its people.  In
            Thailand, respect for the Royal Family goes beyond mere custom however, it is
            safeguarded by law: it is not only socially unacceptable to disparage members of the
            Royal Family or their likenesses; it is punishable under lese majeste law. Furthermore,
            it is required to stand in respect to the King at the commencement of films and to stop
            walking and/or stand during the playing of the national anthem at 8 am and 6 pm. 
            On the lighter side, it has become fashionable to pay respect to the king by wearing
            a yellow shirt on Monday’s since the 60th anniversary of the king’s reign in 2006.
             </p>

            <p style="text-indent:50px;">While both the King’s official residence, the Grand Palace, and his traditional
            residence, Chitralada Palace, are located in Bangkok (where the King has instituted
            an agricultural research center), the King and Queen are typically found at Klai
            Kangwon Villa in the seaside town of Hua Hin.</p>

